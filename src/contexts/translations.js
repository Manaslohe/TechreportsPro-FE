// Helper: convert ASCII digits to Devanagari (use for dynamic values in Hindi mode)
export const toHindiDigits = (input) =>
  String(input).replace(/\d/g, (d) => "०१२३४५६७८९"[d]);

// Helper: format Date to Hindi with Devanagari numerals
export const formatDateHi = (date, options) =>
  new Intl.DateTimeFormat("hi-IN-u-nu-deva", options || { year: "numeric", month: "long", day: "numeric" }).format(date);

